
# 유저 프로필 정보 테이블 생성
CREATE TABLE USER_PROFILE(
	USER_NO INT AUTO_INCREMENT PRIMARY KEY COMMENT "회원번호",
	USER_NAME VARCHAR(30) NOT NULL COMMENT "회원이름",
	VIEW_COUNT INT NOT NULL COMMENT "조회수",
	CREATED_AT DATETIME NOT NULL COMMENT "등록일"
);

