# 유저 포인트 충전 테이블 생성
CREATE TABLE POINT_CHARGE (
    USER_NO INT NOT NULL,
    AMOUNT INT NOT NULL DEFAULT 0,
    PRIMARY KEY (USER_NO),
    CONSTRAINT FK_POINT_CHARGE_USER_NO
        FOREIGN KEY (USER_NO)
        REFERENCES USER_PROFILE(USER_NO)
        ON DELETE CASCADE
);


